// ============================================================================
// קומפוננטת התראה (Alert)
// ----------------------------------------------------------------------------
// תפקיד: להציג הודעה צבעונית למשתמש - הצלחה (ירוק), שגיאה (אדום) או מידע (כחול).
// שימוש: <Alert type="success">נרשמת בהצלחה!</Alert>
// אם אין תוכן (children) - הקומפוננטה לא מציירת כלום.
// ============================================================================

export default function Alert({ type = 'info', children }) {
  if (!children) return null // אין מה להציג

  // אייקון קטן שמתאים לכל סוג הודעה (משפר את הקריאות המיידית)
  const icons = {
    success: '✓',
    danger: '⚠',
    info: 'ℹ',
  }

  return (
    <div className={`alert alert-${type}`} role="alert">
      <span aria-hidden="true">{icons[type]}</span>
      <span>{children}</span>
    </div>
  )
}
